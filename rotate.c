#include "push_swap.h"

void	rotate(t_list **lst)
{
	t_list	*first;

	if (!lst || !(*lst))
		return ;
	first = *lst;
	*lst = first->next;
	first->next = 0;
	ft_lstadd_back(lst, first);
}
